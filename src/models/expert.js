class Expert {
  constructor(
    id,
    nombre,
    apellido,
    areaInvestigacion,
    habilidades,
    costoServicios,
    persona
  ) {
    this.id = id;
    this.nombre = nombre;
    this.apellido = apellido;
    this.areaInvestigacion = areaInvestigacion;
    this.habilidades = habilidades;
    this.costoServicios = costoServicios;
    this.persona = persona;
  }
}

export default Expert;
