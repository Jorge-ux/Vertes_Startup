class Student {
  constructor(
    id,
    nombre,
    apellido,
    areaInvestigacion,
    persona,
    nombreProyecto
  ) {
    this.id = id;
    this.nombre = nombre;
    this.apellido = apellido;
    this.areaInvestigacion = areaInvestigacion;
    this.persona = persona;
    this.nombreProyecto = nombreProyecto;
  }
}

export default Student;
